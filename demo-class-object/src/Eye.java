public class Eye {
  private String color;

  //! Contructor
  public Eye(){

  }

  //! All args constructor
  public Eye(String color){
    this.color = color;
}

  //! getter
  public String getColor(){
    return this.color;
  }

  //! setter
  public void setColor(String color){
    this.color = color;
  }
}
